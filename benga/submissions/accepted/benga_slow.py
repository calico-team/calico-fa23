MOD = 2 ** (3 ** 2) * 3 ** (2 ** 3)


def solve(N: int) -> int:
    A_N = matpowmod(A, N // 3, MOD)
    return (A_N[0][-1] - 1) % MOD


def matpowmod(A, n, mod):
    curr_power = [[A[i][j] for j in range(len(A))] for i in range(len(A))]
    result = [[1 if i == j else 0 for j in range(len(A))] for i in range(len(A))]
    while n:
        if n & 1:
            result = matmulmod(curr_power, result, mod)
        curr_power = matmulmod(curr_power, curr_power, mod)
        n >>= 1
    return result


def matmulmod(A, B, mod):
    AB = [[0] * len(A) for _ in range(len(B[0]))]
    for i in range(len(A)):
        for j in range(len(B[0])):
            for k in range(len(B)):
                AB[i][j] += A[i][k] * B[k][j] % mod
    return AB


