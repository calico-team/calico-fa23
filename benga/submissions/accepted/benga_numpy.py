MOD = 2 ** (3 ** 2) * 3 ** (2 ** 3)


def solve(N: int) -> int:
    B_N = matpowmod(A, N // 3, MOD)
    return (B_N[0][-1] + B_N[0][0] - 1) % MOD


def matpowmod(A, n, mod):
    curr_power = np.copy(A)
    result = np.identity(len(A), dtype=np.int64)
    while n:
        if n & 1:
            result = curr_power @ result % mod
        curr_power = curr_power @ curr_power % mod
        n >>= 1
    return result


